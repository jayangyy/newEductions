package cr.cdrb.web.edu.model;

import org.apache.commons.lang3.StringUtils;

/**
 *
 * @author milord
 */
public class Special_User_Card {
    private String pid;
    private String name;
    private String sex;
    private String card_no;
    private String archives_no;
    private String award_unit;
    private String companyid;
    private String dwid;
    private String dwname;
    private String bmid;
    private String bmname;
    private String optuserid;
    private String optusername;
    private String optdate;

    /**
     * @return the pid
     */
    public String getPid() {
        return pid;
    }

    /**
     * @param pid the pid to set
     */
    public void setPid(String pid) {
        this.pid = pid.toUpperCase();
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the sex
     */
    public String getSex() {
        return sex;
    }

    /**
     * @param sex the sex to set
     */
    public void setSex(String sex) {
        this.sex = sex;
    }

    /**
     * @return the card_no
     */
    public String getCard_no() {
        return card_no;
    }

    /**
     * @param card_no the card_no to set
     */
    public void setCard_no(String card_no) {
        this.card_no = card_no;
    }

    /**
     * @return the archives_no
     */
    public String getArchives_no() {
        return archives_no;
    }

    /**
     * @param archives_no the archives_no to set
     */
    public void setArchives_no(String archives_no) {
        this.archives_no = archives_no;
    }

    /**
     * @return the award_unit
     */
    public String getAward_unit() {
        return award_unit;
    }

    /**
     * @param award_unit the award_unit to set
     */
    public void setAward_unit(String award_unit) {
        this.award_unit = award_unit;
    }

    /**
     * @return the companyid
     */
    public String getCompanyid() {
        return companyid;
    }

    /**
     * @param companyid the companyid to set
     */
    public void setCompanyid(String companyid) {
        this.companyid = companyid;
    }

    /**
     * @return the optuserid
     */
    public String getOptuserid() {
        return optuserid;
    }

    /**
     * @param optuserid the optuserid to set
     */
    public void setOptuserid(String optuserid) {
        this.optuserid = optuserid;
    }

    /**
     * @return the optusername
     */
    public String getOptusername() {
        return optusername;
    }

    /**
     * @param optusername the optusername to set
     */
    public void setOptusername(String optusername) {
        this.optusername = optusername;
    }

    /**
     * @return the optdate
     */
    public String getOptdate() {
        return StringUtils.isBlank(optdate) ? "" : optdate.split(" ")[0];
    }

    /**
     * @param optdate the optdate to set
     */
    public void setOptdate(String optdate) {
        this.optdate = optdate;
    }

    /**
     * @return the dwid
     */
    public String getDwid() {
        return dwid;
    }

    /**
     * @param dwid the dwid to set
     */
    public void setDwid(String dwid) {
        this.dwid = dwid;
    }

    /**
     * @return the dwname
     */
    public String getDwname() {
        return dwname;
    }

    /**
     * @param dwname the dwname to set
     */
    public void setDwname(String dwname) {
        this.dwname = dwname;
    }

    /**
     * @return the bmid
     */
    public String getBmid() {
        return bmid;
    }

    /**
     * @param bmid the bmid to set
     */
    public void setBmid(String bmid) {
        this.bmid = bmid;
    }

    /**
     * @return the bmname
     */
    public String getBmname() {
        return bmname;
    }

    /**
     * @param bmname the bmname to set
     */
    public void setBmname(String bmname) {
        this.bmname = bmname;
    }
}
