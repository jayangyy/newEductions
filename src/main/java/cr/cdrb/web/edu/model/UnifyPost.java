public class UnifyPost {
    private String id;
    private String gzid;
    private String name;

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the code
     */
    public String getGzid() {
        return gzid;
    }

    /**
     * @param code the code to set
     */
    public void setGzid(String gzid) {
        this.gzid = gzid;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }
}
